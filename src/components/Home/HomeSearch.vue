            nomeUsuario: '',
            usuario: []
        }
    },
    methods: {
        buscarUsuario(){
           this.$http(`${this.nomeUsuario}`)
            .then(r => {
                this.usuario = r.data
                this.nomeUsuario = ''
            })
           
        }
    }
}
</script>

